<p>
    Az Overwatch egy a Blizzard Entertainment által fejlesztett és kiadott többjátékos, csapat-orientált
    belső-nézetes lövöldözős videójáték. A játékot a 2014-es BlizzConon jelentették be, és 2016. május 24-én
    került kiadásra.
</p>

<p>
    Az Overwatch története egy alternatív Földön játszódik. 2016-ban az Omnica Corporation nevezetű globális
    iparvállalat létrehozza az önfejlesztő robotokat, az ominceket, de csalás miatt végül a világ több pontján
    felépült gyárait lekapcsolták. 30 évvel
    később egy kiterjedt vírusfertőzés hatására a gyárak önmaguktól újraindultak, és elkezdtek harci ominceket
    előállítani, akik aztán az emberiség ellen fordultak. Ez vezetett az Omnic Crisis-hez. A háború során
    tucatnyi ország próbált egyedül
    helytállni az omnicek által korábban felállított hadseregeivel szemben, sikertelenül. Végül az ENSZ úgy
    döntött, hogy létrehoz egy elit csapásmérő egységet, ez lett az Overwatch. Az új egység hatékonyan vette fel
    a harcot az omnicek ellen
    a világ bármely pontján, és bár végül megnyerve a háborút elhozta a békét az emberiség számára, két
    legjelentősebb tagja, a vezető Jack Morrison és a másodtiszt Gabriel Reyes között komoly ellentét kezdett
    kibontakozni.
</p>

<p>
    Az Overwatch egy kifejezetten csapat-alapú FPS, ahol a pályákon 2 csapat 6-6 játékosa mérkőzik meg a másik
    csapattal.
</p>

<p>
    A játékos tucatnyi hős közül választhat, amelyek mind egyedi képességgel, és előre meghatározott
    szerepkörrel rendelkeznek, továbbá egy három csillagos skálával jelképezett nehézséggel, amely az adott
    hőssel való játék nehézségét mutatja. Minden hős rendelkezik
    minimum egy "végső" képességgel, amelyek nagyban befolyásolhatják a csaták kimenetelét.
</p>

<h4>
    A hősök az alábbi 3 szerepkörbe lettek karegorizálva:
</h4>

<ul>

    <li>
        <strong>Tank:</strong> A tank hősök rendelkeznek a legtöbb életerővel, fizikai méretük és képességeik
        pedig leginkább a többi nem-tank csapattárs elleni sebzés befogására, elhárítására szolgál. Végső
        képességeik is inkább a csapat
        támogatását szolgálja.
    </li>
    <li>
        <strong>Damage:</strong> Ezek a hősök két részre bonthatók; egy részük rendelkezik a legnagyobb
        sebzéssel, végső képességükkel pedig jó időzítéssel akár az ellenséges csapat összes tagjával is
        végezhetnek, míg a másik eredményesen
        védelmezhetik az objektumokat és területeket, illetve tarthatják sakkba a csomópontok figyelésével az
        ellenséget, viszont mindkettőnek kevés életereje van.
    </li>
    <li>
        <strong>Support:</strong> A támogató hősök képesek egy vagy több csapattárs egyidejű gyógyítására és
        felerősítésére. Ők is kevés életerővel rendelkeznek, ezért lehetőleg távol kell maradniuk a közvetlen
        harcoktól, és fedezniük a többieket.
    </li>

</ul>

